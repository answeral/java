package p0617;

public class C0617_02 {

	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		System.out.println(a/(double)b);
		System.out.println((10/3.0));
		System.out.printf("%.2f \n",10/3.0);//printf 는 줄단락 없음 \n줄바꿈, \t는 텝만큼
		System.out.println("aaa"); //%d는 정수 %f는 실수, %s 문자열
		System.out.printf("%x \n",15); //16진수
		System.out.printf("%o \n",15); //8진수
		System.out.printf("%s \n",Integer.toBinaryString(15)); // 2진수->문자열로 변경 후 
		
		
		
		
		
		
		
		
		
		
	}//main

}//class
 